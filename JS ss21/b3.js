
var so = parseInt(prompt("Nhập vào một số từ 2 đến 8:"));

if (so === 2) {
    console.log("Monday");
} else if (so === 3) {
    console.log("Tuesday");
} else if (so === 4) {
    console.log("Wednesday");
} else if (so === 5) {
    console.log("Thursday");
} else if (so === 6) {
    console.log("Friday");
} else if (so === 7) {
    console.log("Saturday");
} else if (so === 8) {
    console.log("Sunday");
} else {
    console.log("Số không hợp lệ. Vui lòng nhập số từ 2 đến 8.");
}
